Introduction to php

What is PHP?
PHP(PHP: Hypertext Preprocessor) is a server side scripting language designed specifically for web development.

To understand its's role, let's first understand hoe it works:
1. When you visit a website the client sends a request to a web server.
2. The server receives the request and processees it.
3. For static websites, the server simply returns HTML files
4. For dynamic websites, the server:
    -Executes PHP code.
    -Generates HTML based on the codes output.
    -Sends the HTML back to the browser.

To write PHP code you need:
1. A web stream_socket_server
2. PHP interpreter
3. Database server(MySQL)
4. Text editor(IDE)

The easiest ways to get started is by installing XAMPP, which includes all these components



